public class Divisions{

	public static void main(String[] args){
		int exp1=6/3, exp2=12/5, exp3=12%5 ;
		double exp4=12/5.0,  exp5=12.0/3.0, exp6=1.0/3, exp7=12.0%3.0 ;
	
//Output of the above Expression		
		System.out.println("The value of  6/3 = " + exp1);
		System.out.println("The value of 12/5 = " + exp2);
		System.out.println("The value of 12%5 = "  + exp3);
		System.out.println("The value of 12/5.0 = " + exp4);
		System.out.println("The value of  12.0/3.0  = " + exp5);
		System.out.println("The value of 1/3 = " + exp6);
		System.out.println("The value of 12.0%3.0 = " + exp7);
		

	
	}

}